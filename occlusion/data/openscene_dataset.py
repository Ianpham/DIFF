"""OpenScene dataset loader with occupancy labels for NavSim v1.

Updated to work with actual data layout:
  - Combined pkl from per-log meta_data pkls
  - Camera images at: sensor_root/{log_name}/{cam_name}/{hash}.jpg
  - LiDAR at: sensor_root/{log_name}/MergedPointCloud/{token}.pcd
  - Occ labels: sparse .npy format (N, 2) → dense (200, 200, 16)
  - ego_dynamic_state adapted from list/dict format
"""
import os
import pickle
import numpy as np
import torch
from torch.utils.data import Dataset
from typing import Dict, List, Optional, Tuple


CAMERA_NAMES = ["CAM_F0", "CAM_L0", "CAM_R0", "CAM_L1", "CAM_R1", "CAM_L2", "CAM_R2", "CAM_B0"]


def sparse_occ_to_dense(sparse_arr: np.ndarray,
                        occ_size: List[int],
                        num_classes: int = 17) -> np.ndarray:
    """Convert OpenScene sparse occ (N, 2) to dense (X, Y, Z) grid.

    Sparse format: col 0 = flat voxel index, col 1 = class label (0-16).
    Note: OpenScene v1.0 mini only contains dynamic object classes
    (motorcycle=6, pedestrian=7, traffic_cone=8, trailer=9, truck=10).
    """
    X, Y, Z = occ_size
    total = X * Y * Z
    dense = np.zeros(total, dtype=np.int64)

    if sparse_arr.shape[0] == 0:
        return dense.reshape(X, Y, Z)

    indices = sparse_arr[:, 0].astype(np.int64)
    labels = sparse_arr[:, 1].astype(np.int64)

    valid = (indices >= 0) & (indices < total)
    dense[indices[valid]] = labels[valid]

    return dense.reshape(X, Y, Z)


def load_pcd(path: str) -> np.ndarray:
    """Load nuPlan/OpenScene .pcd file (binary format).

    nuPlan PCD format:
      FIELDS x y z intensity lidar_info ring
      SIZE   4 4 4 1         1          1
      TYPE   F F F U         U          U
    Returns (N, 5) float32: x, y, z, intensity, ring
    """
    with open(path, "rb") as f:
        header = {}
        while True:
            line = f.readline().decode("utf-8", errors="ignore").strip()
            if line.startswith("DATA"):
                data_type = line.split()[-1]
                break
            parts = line.split(None, 1)
            if len(parts) == 2:
                header[parts[0]] = parts[1]

        n_points = int(header.get("POINTS", "0"))
        fields = header.get("FIELDS", "x y z").split()
        sizes = header.get("SIZE", "4 " * len(fields)).split()
        types = header.get("TYPE", "F " * len(fields)).split()

        if data_type == "binary" and n_points > 0:
            numpy_dtypes = []
            for fn, tp, sz in zip(fields, types, sizes):
                sz = int(sz)
                if tp == "F":
                    numpy_dtypes.append((fn, {4: np.float32, 8: np.float64}.get(sz, np.float32)))
                elif tp == "U":
                    numpy_dtypes.append((fn, {1: np.uint8, 2: np.uint16, 4: np.uint32}.get(sz, np.uint8)))
                elif tp == "I":
                    numpy_dtypes.append((fn, {1: np.int8, 2: np.int16, 4: np.int32}.get(sz, np.int32)))

            dt = np.dtype(numpy_dtypes)
            raw = np.frombuffer(f.read(), dtype=dt, count=n_points)

            pts = np.zeros((n_points, 5), dtype=np.float32)
            for i, fn in enumerate(["x", "y", "z"]):
                if fn in raw.dtype.names:
                    pts[:, i] = raw[fn].astype(np.float32)
            if "intensity" in raw.dtype.names:
                pts[:, 3] = raw["intensity"].astype(np.float32) / 255.0
            if "ring" in raw.dtype.names:
                pts[:, 4] = raw["ring"].astype(np.float32)
            return pts

    # Fallback
    n = np.random.randint(30000, 60000)
    return np.random.uniform(-40, 40, (n, 5)).astype(np.float32)


class OpenSceneOccDataset(Dataset):
    """OpenScene dataset for GaussianFormer3D × TransDiffuser.

    Works with the combined pkl generated by build_dataset.py.
    Backward compatible: if info_file doesn't exist, falls back to dummies.
    """

    CAMERA_NAMES = CAMERA_NAMES

    def __init__(self, data_root, occ_label_root, split="train", info_file=None,
                 img_size=(448, 800), point_cloud_range=None, occ_size=None,
                 num_classes=17, trajectory_length=8, history_length=4,
                 load_occ=True, load_planning=True, sensor_root=None):

        self.data_root = data_root
        self.occ_root = occ_label_root
        self.split = split
        self.img_size = img_size
        self.num_classes = num_classes
        self.traj_len = trajectory_length
        self.hist_len = history_length
        self.load_occ = load_occ
        self.load_planning = load_planning
        self.pc_range = np.array(point_cloud_range or [-40, -40, -1, 40, 40, 5.4])
        self.occ_size = occ_size or [200, 200, 16]

        # sensor_root: where camera/lidar files live
        # If not specified, use data_root (for backward compat)
        self.sensor_root = sensor_root or data_root

        # Load info file
        if info_file is None:
            info_file = os.path.join(data_root, f"openscene_infos_{split}.pkl")

        if os.path.exists(info_file):
            with open(info_file, "rb") as f:
                self.infos = pickle.load(f)
            print(f"[Dataset] Loaded {len(self.infos)} samples from {info_file}")

            # Filter to only samples with occ labels if needed
            if self.load_occ:
                n_before = len(self.infos)
                self.infos = [i for i in self.infos if i.get("_has_occ_on_disk", True)]
                if len(self.infos) < n_before:
                    print(f"[Dataset] Filtered to {len(self.infos)} samples with occ labels "
                          f"(dropped {n_before - len(self.infos)})")
        else:
            n_dummy = 100 if split == "train" else 20
            print(f"Warning: {info_file} not found. Using {n_dummy} dummy samples.")
            self.infos = self._dummy(n_dummy)

    def _dummy(self, n):
        """Generate dummy samples for testing without data."""
        infos = []
        for i in range(n):
            info = {
                "token": f"s_{i:06d}",
                "lidar_path": f"dummy_{i}.bin",
                "ego_status": dict(speed=np.random.uniform(0, 15),
                                   acceleration=np.random.uniform(-3, 3),
                                   yaw_rate=np.random.uniform(-0.3, 0.3),
                                   steering_angle=0.0),
                "cams": {c: {"data_path": f"d_{c}_{i}.jpg",
                             "cam_intrinsic": np.eye(3, dtype=np.float32) * 1000,
                             "lidar2img": np.eye(4, dtype=np.float32)}
                         for c in self.CAMERA_NAMES},
            }
            if self.load_planning:
                info["gt_trajectory"] = np.random.randn(self.traj_len, 2).astype(np.float32) * 2
                info["history_trajectory"] = np.random.randn(self.hist_len, 2).astype(np.float32)
            infos.append(info)
        return infos

    def __len__(self):
        return len(self.infos)

    def __getitem__(self, idx):
        info = self.infos[idx]
        data = {}

        # ---- Camera images ----
        imgs, l2i = [], []
        for cn in self.CAMERA_NAMES:
            ci = info["cams"][cn]
            img_path = os.path.join(self.sensor_root, ci["data_path"])

            if os.path.exists(img_path):
                from PIL import Image
                pil_img = Image.open(img_path).resize(
                    (self.img_size[1], self.img_size[0])
                )
                img = np.array(pil_img).astype(np.float32)
            else:
                img = np.random.randint(
                    0, 256, (*self.img_size, 3), dtype=np.uint8
                ).astype(np.float32)

            # ImageNet mean subtraction
            img -= np.array([103.530, 116.280, 123.675])
            imgs.append(img)
            l2i.append(ci["lidar2img"])

        data["images"] = torch.from_numpy(np.stack(imgs)).permute(0, 3, 1, 2).float()
        data["lidar2img"] = torch.from_numpy(np.stack(l2i)).float()

        # ---- LiDAR points ----
        lidar_path = os.path.join(self.sensor_root, info["lidar_path"])
        if os.path.exists(lidar_path):
            if lidar_path.endswith(".pcd"):
                pts = load_pcd(lidar_path)
            else:
                pts = np.fromfile(lidar_path, dtype=np.float32).reshape(-1, 5)
        else:
            n = np.random.randint(30000, 60000)
            pts = np.concatenate([
                np.random.uniform(self.pc_range[:3], self.pc_range[3:], (n, 3)),
                np.random.uniform(0, 1, (n, 1)),
                np.zeros((n, 1))
            ], axis=1).astype(np.float32)

        data["points"] = torch.from_numpy(pts).float()

        # ---- Occupancy labels ----
        if self.load_occ:
            occ_path = info.get("occ_gt_path", "")

            # Fallback: try old-style token-based lookup
            if not occ_path or not os.path.exists(occ_path):
                occ_path_alt = os.path.join(self.occ_root, f"{info['token']}.npz")
                if os.path.exists(occ_path_alt):
                    od = np.load(occ_path_alt)
                    occ_label = od["occ_label"]
                    data["occ_label"] = torch.from_numpy(occ_label).long()
                else:
                    data["occ_label"] = torch.zeros(self.occ_size, dtype=torch.int64)
            elif os.path.exists(occ_path):
                if occ_path.endswith(".npy"):
                    sparse_occ = np.load(occ_path)
                    dense_occ = sparse_occ_to_dense(sparse_occ, self.occ_size, self.num_classes)
                elif occ_path.endswith(".npz"):
                    od = np.load(occ_path)
                    dense_occ = od.get("occ_label", od.get("semantics",
                                 np.zeros(self.occ_size, dtype=np.int64)))
                else:
                    dense_occ = np.zeros(self.occ_size, dtype=np.int64)
                data["occ_label"] = torch.from_numpy(dense_occ).long()
            else:
                data["occ_label"] = torch.zeros(self.occ_size, dtype=torch.int64)

        # ---- Ego status ----
        e = info.get("ego_status", {})
        if isinstance(e, dict):
            data["ego_status"] = torch.tensor([
                e.get("speed", 0), e.get("acceleration", 0),
                e.get("yaw_rate", 0), e.get("steering_angle", 0),
                0, 0, 0
            ], dtype=torch.float32)
        elif isinstance(e, (list, np.ndarray)):
            vals = list(e) + [0] * max(0, 7 - len(e))
            data["ego_status"] = torch.tensor(vals[:7], dtype=torch.float32)
        else:
            data["ego_status"] = torch.zeros(7, dtype=torch.float32)

        # ---- Planning ----
        if self.load_planning:
            gt = info.get("gt_trajectory", np.zeros((self.traj_len, 2), dtype=np.float32))
            ht = info.get("history_trajectory", np.zeros((self.hist_len, 2), dtype=np.float32))
            if isinstance(gt, list):
                gt = np.array(gt, dtype=np.float32)
            if isinstance(ht, list):
                ht = np.array(ht, dtype=np.float32)
            data["gt_trajectory"] = torch.from_numpy(gt).float()
            data["gt_actions"] = torch.from_numpy(
                np.diff(gt, axis=0, prepend=gt[:1])
            ).float()
            data["history_trajectory"] = torch.from_numpy(ht).float()

        data["token"] = info["token"]
        data["img_shape"] = torch.tensor(self.img_size)

        return data


def collate_fn(batch):
    """Collate function that handles variable-length point clouds."""
    c = {}
    stack_keys = [
        "images", "lidar2img", "occ_label", "ego_status",
        "gt_trajectory", "gt_actions", "history_trajectory", "img_shape",
    ]
    for k in stack_keys:
        if k in batch[0]:
            c[k] = torch.stack([b[k] for b in batch])
    if "points" in batch[0]:
        c["points"] = [b["points"] for b in batch]
    if "token" in batch[0]:
        c["token"] = [b["token"] for b in batch]
    return c